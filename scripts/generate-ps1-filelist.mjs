#!/usr/bin/env node
/**
 * Fetch Pan-STARRS DR2 file lists for all catalog objects with coordinates
 * and dec > -30, then write a static TypeScript lookup table.
 *
 * Output: src/data/ps1-files.ts
 *
 * Usage:
 *   node scripts/generate-ps1-filelist.mjs
 */

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const sleep = ms => new Promise(r => setTimeout(r, ms))

// ── Parse catalogs ───────────────────────────────────────────────────────────

function parseMessier() {
  const src = readFileSync(resolve(__dirname, '../src/data/messier.ts'), 'utf-8')
  const results = []
  for (const match of src.matchAll(/\{[^}]+messier:\s*\d+[^}]+\}/gs)) {
    const block = match[0]
    const num = block.match(/messier:\s*(\d+)/)?.[1]
    const ra  = block.match(/ra:\s*([\d.]+)/)?.[1]
    const dec = block.match(/dec:\s*(-?[\d.]+)/)?.[1]
    if (num && ra && dec) {
      results.push({ id: `m${num}`, ra: parseFloat(ra), dec: parseFloat(dec) })
    }
  }
  return results
}

function parseDeepSky() {
  const src = readFileSync(resolve(__dirname, '../src/data/deep-sky.ts'), 'utf-8')
  const results = []
  for (const match of src.matchAll(/\{[^}]+id:\s*['"][^'"]+['"][^}]+\}/gs)) {
    const block = match[0]
    const id  = block.match(/id:\s*['"]([^'"]+)['"]/)?.[1]
    const ra  = block.match(/ra:\s*([\d.]+)/)?.[1]
    const dec = block.match(/dec:\s*(-?[\d.]+)/)?.[1]
    if (id && ra && dec) {
      results.push({ id, ra: parseFloat(ra), dec: parseFloat(dec) })
    }
  }
  return results
}

function parseStars() {
  const src = readFileSync(resolve(__dirname, '../src/data/stars.ts'), 'utf-8')
  const results = []
  for (const match of src.matchAll(/\{[^}]+id:\s*['"][^'"]+['"][^}]+ra:\s*[\d.]+[^}]+\}/gs)) {
    const block = match[0]
    const id  = block.match(/id:\s*['"]([^'"]+)['"]/)?.[1]
    const ra  = block.match(/ra:\s*([\d.]+)/)?.[1]
    const dec = block.match(/dec:\s*(-?[\d.]+)/)?.[1]
    if (id && ra && dec) {
      results.push({ id, ra: parseFloat(ra), dec: parseFloat(dec) })
    }
  }
  return results
}

const allObjects = [...parseMessier(), ...parseDeepSky(), ...parseStars()]
  .filter(o => o.dec > -30) // Pan-STARRS coverage limit

console.error(`Objects with dec > -30: ${allObjects.length}`)

// ── Fetch PS1 file lists ─────────────────────────────────────────────────────

async function fetchFileList(ra, dec) {
  const url = new URL('https://ps1images.stsci.edu/cgi-bin/ps1filenames.py')
  url.searchParams.set('ra', ra.toFixed(6))
  url.searchParams.set('dec', dec.toFixed(6))
  url.searchParams.set('filters', 'grizy')
  url.searchParams.set('type', 'stack')

  const res = await fetch(url.toString(), {
    headers: { 'User-Agent': 'cosmos-lib-ps1-generator/1.0' },
    signal: AbortSignal.timeout(15000),
  })
  if (!res.ok) return null

  const lines = (await res.text()).trim().split('\n').slice(1)
  if (lines.length === 0) return null

  const byFilter = {}
  for (const line of lines) {
    const cols = line.trim().split(/\s+/)
    const filter   = cols[4]
    const filename = cols[7]
    if (filter && filename && !byFilter[filter]) {
      byFilter[filter] = filename
    }
  }

  return Object.keys(byFilter).length > 0 ? byFilter : null
}

const results = {}
let found = 0
let failed = 0

for (let i = 0; i < allObjects.length; i++) {
  const obj = allObjects[i]
  const pct = ((i / allObjects.length) * 100).toFixed(0).padStart(3)
  process.stderr.write(`\r[${pct}%] ${found}/${i} ${obj.id.padEnd(20)}`)

  try {
    const files = await fetchFileList(obj.ra, obj.dec)
    if (files) {
      results[obj.id] = files
      found++
    } else {
      failed++
    }
  } catch {
    failed++
  }

  await sleep(300) // polite rate limiting
}

process.stderr.write('\n')
console.error(`Done: ${found} found, ${failed} failed`)

// ── Write output ─────────────────────────────────────────────────────────────

const entries = Object.entries(results)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([id, filters]) => {
    const filterEntries = Object.entries(filters)
      .map(([f, name]) => `${f}: '${name}'`)
      .join(', ')
    return `  '${id}': { ${filterEntries} }`
  })
  .join(',\n')

const output = `/**
 * Precomputed Pan-STARRS DR2 file list for catalog objects.
 *
 * Maps object ID to per-filter filenames from the PS1 archive.
 * Used by the runtime cutout module to skip the file-list API call,
 * reducing Pan-STARRS cutout requests from 2 round-trips to 1.
 *
 * Generated by: scripts/generate-ps1-filelist.mjs
 * DO NOT EDIT MANUALLY.
 */
export const PS1_FILES: Readonly<Record<string, Readonly<Record<string, string>>>> = {
${entries},
}
`

const outPath = resolve(__dirname, '../src/data/ps1-files.ts')
writeFileSync(outPath, output)
console.error(`Written: ${outPath} (${entries.split('\n').length} entries)`)
